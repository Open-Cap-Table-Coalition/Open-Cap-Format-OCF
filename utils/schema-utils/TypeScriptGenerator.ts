/**
 * TypeScriptGenerator
 *
 * Bespoke, convention-aware TypeScript codegen for OCF schemas. Builds on the
 * `SchemaComposer` so that each object's inherited (`allOf`) properties are
 * already flattened, then emits one named TS declaration per schema:
 *
 *   - enums              -> `export type Name = "A" | "B" | ...`
 *   - scalar types       -> `export type Name = string | number | boolean`
 *   - object/type/file   -> `export interface Name { ... }`
 *
 * Property values are mapped structurally, following OCF conventions:
 *
 *   - `$ref`             -> the named type for the referenced schema
 *   - `const`            -> a literal type (`"TX_STOCK_ISSUANCE"`)
 *   - inline `enum`      -> a union of literals
 *   - `oneOf` / `anyOf`  -> a union of the member types
 *   - `type: "array"`    -> `Item[]`
 *   - nested object      -> an inline object type
 *
 * Because every schema becomes a named type and `$ref`s become references to
 * those names, the output mirrors the OCF schema graph rather than inlining
 * everything â€” far more readable and maintainable than generic JSON-Schema
 * codegen. Output is a single self-contained module (declaration order does
 * not matter for `type`/`interface` declarations).
 *
 * The abstract "primitive" base schemas are omitted by default: they exist
 * only to be inherited via `allOf`, and the composer has already flattened
 * their properties into each concrete type, so nothing references them. Pass
 * `includePrimitives: true` to emit them anyway.
 *
 * Framework-free: plain schema JSON in, a TypeScript source string out.
 */

import {
  buildSchemaRegistry,
  composeAll,
  ComposedSchemaJson,
  isBackwardsCompatibleWrapper,
  RawSchemaJson,
} from "./SchemaComposer.js";

export type GenerateTypeScriptOptions = {
  /** Prefix applied to every generated type name. Defaults to `"OCF"`, which
   *  keeps names collision-free against TS/DOM globals like `Object`, `Date`,
   *  and `File` (all of which exist as OCF schema names). */
  typePrefix?: string;
  /** Emit JSDoc comments from schema/property `description`s. Default `true`. */
  includeDescriptions?: boolean;
  /** Banner comment placed at the top of the file. A sensible default is used
   *  when omitted; pass `""` to suppress. */
  banner?: string;
  /** Behavior when a `$ref` can't be resolved: `"unknown"` (default) emits
   *  the `unknown` type and records a warning; `"throw"` fails. */
  onMissingRef?: "unknown" | "throw";
  /** Emit the abstract "primitive" base schemas (the `allOf` inheritance bases
   *  under `/schema/primitives`). Default `false`: their properties are
   *  flattened into every concrete type via composition, so nothing references
   *  them â€” emitting them only adds orphaned, easily-misused base shapes. */
  includePrimitives?: boolean;
};

const DEFAULT_PREFIX = "OCF";

const DEFAULT_BANNER = `/**
 * Open Cap Format (OCF) â€” TypeScript types.
 *
 * AUTO-GENERATED by \`npm run schema:gen-types\`. Do not edit by hand;
 * regenerate from the JSON Schemas under /schema after any change.
 */
/* eslint-disable */`;

/** Short notice placed under the banner so consumers of the generated module
 *  understand the primitives policy â€” why there are no abstract base types
 *  (or, conversely, why they are present) and how to flip it. */
function primitivesNotice(includePrimitives: boolean): string {
  return includePrimitives
    ? `/* NOTE: the abstract "primitive" base schemas (under /schema/primitives)
 * are INCLUDED below (generated with --include-primitives). They are
 * inheritance-only bases; a bare base type is never itself a valid OCF value. */`
    : `/* NOTE: the abstract "primitive" base schemas (the \`allOf\` inheritance
 * bases under /schema/primitives) are intentionally omitted. Their properties
 * are flattened into each concrete type, so nothing references them and a bare
 * base type is never itself a valid OCF value. Regenerate with
 * \`--include-primitives\` if you need the base shapes. */`;
}

/** The five top-level partitions of /schema, used to group output. */
const CATEGORY_ORDER = [
  "enums",
  "types",
  "primitives",
  "objects",
  "files",
] as const;
type Category = typeof CATEGORY_ORDER[number] | "other";

const CATEGORY_HEADINGS: { [k in Category]: string } = {
  enums: "Enums",
  types: "Types",
  primitives: "Primitives (abstract base shapes)",
  objects: "Objects",
  files: "File wrappers",
  other: "Other",
};

// ---------------------------------------------------------------------------
// Naming
// ---------------------------------------------------------------------------

function basenameFromId(id: string): string {
  const last = id.split("/").pop() ?? id;
  return last.replace(/\.schema\.json$/, "");
}

/** The `$id` path segment that places a schema in a /schema partition. */
function categoryFromId(id: string): Category {
  const marker = "/schema/";
  const idx = id.indexOf(marker);
  const tail = idx >= 0 ? id.slice(idx + marker.length) : id;
  const segment = tail.split("/")[0];
  return (CATEGORY_ORDER as readonly string[]).includes(segment)
    ? (segment as Category)
    : "other";
}

/** A schema's partition rank in `CATEGORY_ORDER`; `"other"` sorts last. Used
 *  to decide, on a base-name collision, which schema keeps the clean name. */
function categoryPriority(id: string): number {
  const idx = (CATEGORY_ORDER as readonly string[]).indexOf(categoryFromId(id));
  return idx === -1 ? CATEGORY_ORDER.length : idx;
}

/** Transaction object schemas live under `/schema/objects/transactions/`. Used
 *  to split the `objects` partition into `AnyTransaction` vs the rest. */
function isTransactionId(id: string): boolean {
  return id.includes("/objects/transactions/");
}

/** The literal string value(s) of a discriminant property (`object_type` /
 *  `file_type`) on a composed schema: handles `const` (one value) and inline
 *  `enum` (the back-compat case, where a concrete type accepts both its legacy
 *  and current `object_type`). Returns `[]` when there is no such discriminant. */
function discriminantLiterals(
  schema: ComposedSchemaJson,
  key: string
): string[] {
  const prop = (schema.properties as any)?.[key];
  if (!prop || typeof prop !== "object") return [];
  if ("const" in prop && typeof prop.const === "string") return [prop.const];
  if (Array.isArray(prop.enum)) {
    return prop.enum.filter((v: any): v is string => typeof v === "string");
  }
  return [];
}

function toPascalCase(value: string): string {
  const cleaned = value
    .replace(/[^A-Za-z0-9]+(.)?/g, (_m, c) => (c ? c.toUpperCase() : ""))
    .replace(/[^A-Za-z0-9]/g, "");
  return cleaned ? cleaned.charAt(0).toUpperCase() + cleaned.slice(1) : cleaned;
}

/**
 * Assign a unique TypeScript type name to every schema `$id`. Names are
 * `<prefix><PascalCaseBasename>`. On the rare base-name collision (two schemas
 * with the same basename in different partitions, e.g. `types/File` and
 * `primitives/files/File`), the schema in the earliest partition
 * (`CATEGORY_ORDER`: enums < types < primitives < objects < files) keeps the
 * clean name and the others fold in their parent directory segment to
 * disambiguate (`OCFFile` and `OCFFilesFile`). A schema whose name already
 * begins with the prefix is not double-prefixed (e.g. `OCFManifestFile` ->
 * `OCFManifestFile`, not `OCFOCFManifestFile`).
 */
function assignTypeNames(
  schemas: RawSchemaJson[],
  prefix: string
): Map<string, string> {
  const prefixed = (name: string) =>
    prefix && name.startsWith(prefix) ? name : prefix + name;
  const baseName = (id: string) => prefixed(toPascalCase(basenameFromId(id)));
  const foldedName = (id: string) => {
    const parts = id.split("/");
    const parent = parts[parts.length - 2] ?? "";
    return prefixed(toPascalCase(parent) + toPascalCase(basenameFromId(id)));
  };

  // Assign in partition-priority order so the earliest-partition schema claims
  // the clean base name and any later collider falls back to its folded name.
  // `$id` breaks ties for determinism, independent of input order.
  const ordered = [...schemas].sort((a, b) => {
    const pa = categoryPriority(a.$id);
    const pb = categoryPriority(b.$id);
    return pa !== pb ? pa - pb : a.$id.localeCompare(b.$id);
  });

  const idToName = new Map<string, string>();
  const assigned = new Set<string>();
  for (const schema of ordered) {
    let name = assigned.has(baseName(schema.$id))
      ? foldedName(schema.$id)
      : baseName(schema.$id);
    // Final safety net against any residual duplication.
    let unique = name;
    let n = 2;
    while (assigned.has(unique)) unique = `${name}${n++}`;
    assigned.add(unique);
    idToName.set(schema.$id, unique);
  }
  return idToName;
}

