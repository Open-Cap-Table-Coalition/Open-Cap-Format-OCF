/** A `=`-bordered section banner â€” the rule-band placed between output sections. */
function sectionHeading(title: string): string {
  const rule = "=".repeat(73);
  return `// ${rule}\n// ${title}\n// ${rule}`;
}

/** Render `export type Name =\n  | A\n  | B;` (or `never` when empty). Shared by
 *  the enum declarer and the aggregate unions; `members` are emitted verbatim. */
function unionType(name: string, members: string[], doc = ""): string {
  const body = members.length
    ? members.map((m) => `  | ${m}`).join("\n")
    : "  never";
  return `${doc}export type ${name} =\n${body};`;
}

function declareEnum(name: string, schema: ComposedSchemaJson): string {
  return unionType(
    name,
    (schema.enum as any[]).map((member) => JSON.stringify(member))
  );
}

function declareInterface(
  name: string,
  schema: ComposedSchemaJson,
  ctx: Context,
  includeDescriptions: boolean
): string {
  const required = new Set<string>(schema.required ?? []);
  const entries = Object.entries(schema.properties ?? {});
  const lines = entries.map(([key, value]: [string, any]) => {
    const doc = includeDescriptions
      ? jsdoc([value?.description, ...annotationTags(value ?? {})], "  ")
      : "";
    const optional = required.has(key) ? "" : "?";
    return `${doc}  ${propertyKey(key)}${optional}: ${tsTypeForValue(
      value,
      ctx
    )};`;
  });

  if (schema.additionalProperties && schema.additionalProperties !== true) {
    lines.push(
      `  [key: string]: ${tsTypeForValue(schema.additionalProperties, ctx)};`
    );
  } else if (schema.additionalProperties === true) {
    lines.push(`  [key: string]: unknown;`);
  }

  const body = lines.length ? `\n${lines.join("\n")}\n` : "\n";
  return `export interface ${name} {${body}}`;
}

/**
 * Render a backwards-compatibility wrapper as an alias to the schema it wraps,
 * with the `object_type` discriminant pinned to the wrapper's literal. Using an
 * intersection (`Parent & { object_type: "..." }`) keeps every inherited
 * property and narrows the parent's discriminant union down to the single
 * legacy value, which is exactly what the `allOf` + `const` override means.
 */
function declareWrapperAlias(
  name: string,
  schema: ComposedSchemaJson,
  ctx: Context,
  includeDescriptions: boolean
): string {
  const parentRef = (schema.allOf?.[0] as { $ref?: string } | undefined)?.$ref;
  const parentName = parentRef ? ctx.idToName.get(parentRef) : undefined;
  const objectType = (schema.properties as any)?.object_type ?? {};
  const literal = tsTypeForValue(objectType, ctx);
  const memberDoc = includeDescriptions
    ? jsdoc([objectType.description], "  ")
    : "";
  const member = `${memberDoc}  object_type: ${literal};`;

  if (!parentName) {
    if (ctx.onMissingRef === "throw") {
      throw new Error(
        `Unresolved wrapper parent in codegen: ${parentRef ?? "(none)"}`
      );
    }
    ctx.warnings.push(
      `Unresolved wrapper parent -> object_type only: ${schema.$id}`
    );
    return `export type ${name} = {\n${member}\n};`;
  }

  return `export type ${name} = ${parentName} & {\n${member}\n};`;
}

function declareSchema(
  schema: ComposedSchemaJson,
  ctx: Context,
  includeDescriptions: boolean
): string {
  const name = ctx.idToName.get(schema.$id)!;
  const doc = includeDescriptions
    ? jsdoc([schema.title, schema.description], "")
    : "";

  // Backwards-compat wrapper (a single `allOf` parent plus only an
  // `object_type` discriminant): emit an alias to the parent with the
  // discriminant pinned. These schemas are intentionally left un-flattened by
  // the composer, so treating them as a normal interface here would drop every
  // inherited property and produce a near-empty, useless type.
  if (isBackwardsCompatibleWrapper(schema)) {
    return doc + declareWrapperAlias(name, schema, ctx, includeDescriptions);
  }

  // Enum
  if (Array.isArray(schema.enum)) {
    return doc + declareEnum(name, schema);
  }

  // Object / interface (objects, files, object-shaped types â€” anything with
  // its own properties after composition)
  const hasProperties =
    schema.properties && Object.keys(schema.properties).length > 0;
  if (schema.type === "object" || hasProperties) {
    return doc + declareInterface(name, schema, ctx, includeDescriptions);
  }

  // Root-level union (not used by OCF roots today, but handle it)
  if (Array.isArray(schema.oneOf) || Array.isArray((schema as any).anyOf)) {
    const expr = tsTypeForValue(
      { oneOf: schema.oneOf ?? (schema as any).anyOf },
      ctx
    );
    return `${doc}export type ${name} = ${expr};`;
  }

  // Scalar type alias (Date, Numeric, CountryCode, ...)
  if (typeof schema.type === "string") {
    const docWithTags = includeDescriptions
      ? jsdoc([schema.title, schema.description, ...annotationTags(schema)], "")
      : "";
    return `${docWithTags}export type ${name} = ${scalarType(schema.type)};`;
  }

  ctx.warnings.push(`Unrecognized schema shape -> unknown: ${schema.$id}`);
  return `${doc}export type ${name} = unknown;`;
}

