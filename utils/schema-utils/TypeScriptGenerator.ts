const IDENTIFIER_RE = /^[A-Za-z_$][A-Za-z0-9_$]*$/;

function propertyKey(key: string): string {
  return IDENTIFIER_RE.test(key) ? key : JSON.stringify(key);
}

function scalarType(jsonType: string): string {
  switch (jsonType) {
    case "integer":
    case "number":
      return "number";
    case "boolean":
      return "boolean";
    case "null":
      return "null";
    case "string":
    default:
      return "string";
  }
}

/** A type expression that must be parenthesized before appending `[]`. */
function needsParens(expr: string): boolean {
  return expr.includes("|") || expr.includes("&") || expr.includes(" ");
}

function uniqueUnion(members: string[]): string {
  const seen = new Set<string>();
  const ordered: string[] = [];
  for (const member of members) {
    if (!seen.has(member)) {
      seen.add(member);
      ordered.push(member);
    }
  }
  return ordered.length ? ordered.join(" | ") : "never";
}

type Context = {
  idToName: Map<string, string>;
  /** Schema `$id`s intentionally not emitted (e.g. omitted primitives). A
   *  direct `$ref` to one of these resolves to `unknown` with a warning,
   *  rather than a dangling reference to an undeclared type. */
  omittedIds: Set<string>;
  onMissingRef: "unknown" | "throw";
  warnings: string[];
};

/** Map a single JSON-Schema property value to a TypeScript type expression. */
function tsTypeForValue(value: any, ctx: Context): string {
  if (value === null || typeof value !== "object") return "unknown";

  if (typeof value.$ref === "string") {
    const name = ctx.idToName.get(value.$ref);
    if (name) return name;
    // A direct $ref to a schema we intentionally omitted (e.g. a primitive
    // base referenced outside of an allOf). Surface it rather than dangle.
    if (ctx.omittedIds.has(value.$ref)) {
      if (ctx.onMissingRef === "throw") {
        throw new Error(`$ref to omitted primitive base: ${value.$ref}`);
      }
      ctx.warnings.push(
        `$ref to omitted primitive base -> unknown: ${value.$ref}`
      );
      return "unknown";
    }
    if (ctx.onMissingRef === "throw") {
      throw new Error(`Unresolved $ref in codegen: ${value.$ref}`);
    }
    ctx.warnings.push(`Unresolved $ref -> unknown: ${value.$ref}`);
    return "unknown";
  }

  if ("const" in value) return JSON.stringify(value.const);

  if (Array.isArray(value.enum)) {
    return uniqueUnion(value.enum.map((member: any) => JSON.stringify(member)));
  }

  if (Array.isArray(value.oneOf)) {
    return uniqueUnion(
      value.oneOf.map((member: any) => tsTypeForValue(member, ctx))
    );
  }
  if (Array.isArray(value.anyOf)) {
    return uniqueUnion(
      value.anyOf.map((member: any) => tsTypeForValue(member, ctx))
    );
  }
  if (Array.isArray(value.allOf)) {
    const parts = value.allOf.map((member: any) => tsTypeForValue(member, ctx));
    return parts.length ? parts.join(" & ") : "unknown";
  }

  const jsonType = value.type;

  if (Array.isArray(jsonType)) {
    return uniqueUnion(jsonType.map((t: string) => scalarType(t)));
  }

  if (jsonType === "array") {
    const item = value.items ? tsTypeForValue(value.items, ctx) : "unknown";
    return needsParens(item) ? `(${item})[]` : `${item}[]`;
  }

  if (jsonType === "object" || value.properties) {
    return inlineObjectType(value, ctx);
  }

  if (typeof jsonType === "string") return scalarType(jsonType);

  return "unknown";
}

/** Render an anonymous nested object schema as an inline TS object type. */
function inlineObjectType(value: any, ctx: Context): string {
  const properties = value.properties ?? {};
  const required = new Set<string>(value.required ?? []);
  const members = Object.entries(properties).map(([key, propValue]) => {
    const optional = required.has(key) ? "" : "?";
    return `${propertyKey(key)}${optional}: ${tsTypeForValue(propValue, ctx)};`;
  });
  if (value.additionalProperties && value.additionalProperties !== true) {
    members.push(
      `[key: string]: ${tsTypeForValue(value.additionalProperties, ctx)};`
    );
  } else if (value.additionalProperties === true) {
    members.push(`[key: string]: unknown;`);
  }
  return members.length ? `{ ${members.join(" ")} }` : "Record<string, never>";
}

// ---------------------------------------------------------------------------
// JSDoc
// ---------------------------------------------------------------------------

function jsdoc(
  lines: Array<string | undefined | null | false>,
  indent: string
): string {
  const content = lines.filter(Boolean) as string[];
  if (!content.length) return "";
  // Split any embedded newlines so each becomes its own ` * ` line.
  const flattened = content.flatMap((line) => line.split("\n"));
  const body = flattened.map((line) => `${indent} * ${line}`).join("\n");
  return `${indent}/**\n${body}\n${indent} */\n`;
}

/** Annotation tags (format/pattern/min*) worth surfacing in a type's JSDoc. */
function annotationTags(value: any): string[] {
  const tags: string[] = [];
  if (typeof value.format === "string") tags.push(`@format ${value.format}`);
  if (typeof value.pattern === "string") tags.push(`@pattern ${value.pattern}`);
  if (value.minimum !== undefined) tags.push(`@minimum ${value.minimum}`);
  if (value.maximum !== undefined) tags.push(`@maximum ${value.maximum}`);
  if (value.minItems !== undefined) tags.push(`@minItems ${value.minItems}`);
  if (value.maxItems !== undefined) tags.push(`@maxItems ${value.maxItems}`);
  if (value.minLength !== undefined) tags.push(`@minLength ${value.minLength}`);
  if (value.maxLength !== undefined) tags.push(`@maxLength ${value.maxLength}`);
  if (value.uniqueItems) tags.push(`@uniqueItems`);
  if (value.deprecated) tags.push(`@deprecated`);
  return tags;
}

